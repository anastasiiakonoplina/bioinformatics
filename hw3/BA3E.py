def suf(string):
	return string[1:]

def pref(string):
	k = len(string)
	return string[0:k-1]

def compositionGraph(dna):
	patterns = []
	result = {}
	res = []
	for string in dna:
		patterns.append([pref(string), suf(string)])
	for pattern in patterns:
		if pattern[0] not in result.keys():
			result[pattern[0]] = pattern[1]
		else:
			result[pattern[0]] = ','.join([result[pattern[0]], pattern[1]])
	keys = sorted(result.keys())
	for key in keys:
		res.append(' -> '.join([key, result[key]]))
	res = '\n'.join(res)
	return res

