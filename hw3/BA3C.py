	result = []
	k = len(strings[0])
	for string in strings:
		rest = [word for word in strings if word != string] 
		for word in rest:
			if word[0:k-1] == string[len(string)-k+1:len(string)]:
				res.append([string, word])
	for pair in res:
		result.append(" -> ".join(pair))
	result = sorted(result)
	result = '\n'.join(result)
	return result

